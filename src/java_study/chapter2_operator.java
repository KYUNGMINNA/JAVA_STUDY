package java_study;

public class chapter2_operator {
    public static void main(String[] args) {

        int a=10,b=3;

        //산술 연산자
        System.out.println("산술 연산자");
        System.out.println("a: "+a+" "+"b: "+b);
        System.out.println("a+b "+(a+b));
        System.out.println("a-b "+(a-b));
        System.out.println("a*b "+(a*b));
        System.out.println("a/b "+(a/b));
        System.out.println("a%b "+(a%b));
        System.out.println("a<<b "+(a<<b));
        System.out.println("a>>b "+(a>>b));

        //비교 연산자
        System.out.println("비교 연산자");
        System.out.println("a: "+a+" "+"b: "+b);
        System.out.println("a>b "+(a>b));
        System.out.println("a<b "+(a<b));
        System.out.println("a>=b "+(a>=b));
        System.out.println("a<=b "+(a<=b));
        System.out.println("a==b "+(a==b));
        System.out.println("a!=b "+(a!=b));



    }
}
